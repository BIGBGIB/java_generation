package aula_02_operadores;

public class OperadoresUnarios {

	public static void main(String[] args) {
		
		int contador = 1000;
		
		System.out.println("Valor inical de contador: " + contador);
		
		System.out.println("Valor Pré Incrementado: " + (++ contador));
		
		System.out.println("Vlor Pós Incremento: " + (contador ++));
		
		System.out.println("Novo Valor de contado: " + contador);

	}

}
